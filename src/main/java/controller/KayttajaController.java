package controller;

import model.DAO.KayttajaDAO;
import model.enteties.Kayttaja;

public class KayttajaController {

    private KayttajaDAO kayttajaDAO;

    public KayttajaController() {
        kayttajaDAO = new KayttajaDAO();
    }

    // Method to add a new user
    public void lisaaKayttaja(String etunimi, String sukunimi, String sposti, String puh, String rooli, String salasana) {
        Kayttaja kayttaja = new Kayttaja(0, etunimi, sukunimi, sposti, puh, rooli, salasana); // kayttajaId is set to 0, as it should be auto-generated by the DB
        kayttajaDAO.persist(kayttaja);
    }

    // Method to find a user by ID
    public Kayttaja haeKayttajaById(int id) {
        return kayttajaDAO.findById(id);
    }

    // Method to update a user by ID
    public void paivitaKayttaja(int id, String etunimi, String sukunimi, String sposti, String puh, String rooli, String salasana) {
        Kayttaja kayttaja = new Kayttaja(id, etunimi, sukunimi, sposti, puh, rooli, salasana);
        kayttajaDAO.updateById(id, kayttaja);
    }

    // Method to delete a user
    public void poistaKayttaja(int id) {
        Kayttaja kayttaja = kayttajaDAO.findById(id);
        if (kayttaja != null) {
            kayttajaDAO.remove(kayttaja);
        }
    }
}
